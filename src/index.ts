/**
 * As the name says.
 * @param {Number} input Input number
 * @returns {Boolean} Is odd number
 */
const IsOdd = (input: number): boolean => input % 2 === 1;

/**
 * As the name says.
 * @param {Number} input Input number
 * @returns {Boolean} Is even number
 */
const IsEven = (input: number): boolean => input % 2 === 0;

export { IsOdd, IsEven };
